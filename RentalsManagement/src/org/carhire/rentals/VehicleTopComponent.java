    public static final class VehicleTableModel extends AbstractTableModel{
        private Vector<Vehicle> data = new Vector<Vehicle>();
        private String[] columns = {"Vehicle Status","Vehicle Class"," Vehicle Description","Vehicle Availability"};
        

        public Vehicle getRow(int row) {
            //throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
            return data.get(row);
        }

        @Override
        public int getColumnCount() {
            return columns.length;
        }
        
        @Override
        public String getColumnName(int col){
            return columns[col];
        }

        @Override
        public Object getValueAt(int rowIndex, int columnIndex) {
            Vehicle vehicle = data.get(rowIndex);
            switch(columnIndex){
                case 0:
                    return vehicle.getStatus();
                case 1:
                    return vehicle.getCarclass();
                case 2:
                    return vehicle.getDescription();
                case 3:
                    return vehicle.getAvailability();
            }
            return "";
        }

        @Override
        public int getRowCount() {
            return data.size();
        }
        
        public Vector<Vehicle> getData(){
            return data;
        }
       
        
    }
